# ------------------ PYTHON BASICS NOTES ------------------

# What is Python?
# Python is a high-level, easy-to-learn programming language.
# It is used for web apps, data science, AI, games, automation, etc.

# What is Programming Language?
# A programming language is a way to talk to a computer.
# We give instructions in a language (like Python, Java, C) 
# and computer follows them.

# What is Language?
# Language is just a system of communication.
# Humans use English/Hindi, computers use programming languages.

# How to install Python?
# 1. Go to: https://www.python.org/downloads/
# 2. Download Python (latest version).
# 3. Install it → Tick "Add Python to PATH" option → Finish.

# How to install VS Code?
# 1. Go to: https://code.visualstudio.com/download
# 2. Download VS Code (free).
# 3. Install and open it.
# 4. Inside VS Code → Install "Python Extension" for coding.

# Done! Now you can write Python programs in VS Code.
# Example:
print("Hello, Python!")   # This prints text on screen
